#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*tmp;

	tmp = NULL;
	while (*s)
	{
		if (*s == (char)c)
			tmp = (char *)s;
		s += 1;
	}
	if ((char)c == '\0')
		return ((char *)s);
	return (tmp);
}
